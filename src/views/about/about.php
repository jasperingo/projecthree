	<div class="about-box">
		<div class="about-box-header">Application</div>
		
		Project3 is a Project management system which is aimed at making school project easier, faster and more efficient. 
		<br/>
		It provides students and supervisors with a simple and publicly accessable platform for handling projects. 
		<br/><br/>
		Features include:
		<ul>
			<li>Create a project</li>
			<li>Edit the project details</li>
			<li>Upload project documents</li>
			<li>Communicate through project messages</li>
			<li>Approve or disapproving project documents</li>
			<li>Access and download project documents</li>
			<li>Give reviews on projects</li>
			<li>Browse through different universities</li>
			<li>Request for your university to be added</li>
		</ul>
	</div>

	<div class="about-box">
		<div class="about-box-header">Accessability</div>

		Project3 also provides a public interface for anybody to access completed project with or without a Project3 account. <br/>
		Users can browse through available universities or use the search interface to search for key words.
